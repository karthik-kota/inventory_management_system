CREATE SEQUENCE PRODSEQ
AS
INT
START WITH 1

CREATE SEQUENCE ORDSEQ
AS
INT
START WITH 1

CREATE SEQUENCE SUPSEQ
AS
INT
START WITH 1


------------------------------------------------------CUSTOMER TABLE---------------------------------------------------
CREATE TABLE CUSTOMER(
CUSTOMER_ID VARCHAR(30) PRIMARY KEY DEFAULT 'CUS'+CAST(( NEXT VALUE FOR CUSTSEQ) AS VARCHAR(3)),
CUSTOMER_NAME VARCHAR(40),
CUSTOMER_ADDR VARCHAR(40),
CUSTOMER_EMAIL VARCHAR(30)
)
