------------------------------------------------------SUPPLIER TABLE---------------------------------------------------
CREATE TABLE SUPPLIER(
SUPPLIER_ID VARCHAR(30) PRIMARY KEY DEFAULT 'SUP'+CAST(( NEXT VALUE FOR SUPSEQ) AS VARCHAR(3)),
SUPPLIER_NAME VARCHAR(40),
SUPPLIER_ADDR VARCHAR(40),
SUPPLIER_EMAIL VARCHAR(30)
)
