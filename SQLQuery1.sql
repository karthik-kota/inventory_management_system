------------------------------------------------------ORDERS TABLE---------------------------------------------------
CREATE TABLE ORDERS(
ORDER_ID VARCHAR(30) PRIMARY KEY DEFAULT 'ORD'+CAST(( NEXT VALUE FOR ORDSEQ) AS VARCHAR(3)),
PRODUCT_ID VARCHAR(30) FOREIGN KEY REFERENCES PRODUCT(PRODUCT_ID),
CUSTOMER_ID VARCHAR(30) FOREIGN KEY REFERENCES CUSTOMER(CUSTOMER_ID),
CUSTOMER_EMAIL VARCHAR(30)
)
